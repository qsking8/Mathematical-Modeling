function [ans,tmp] = calculate_ans(path,mydata)
    n = length(path);  % 学生个数
    nums = n  / 4;   % 寝室个数
    path = reshape(path,4,nums);  % 每四个人分配到一个寝室
    tmp = zeros(1,nums);  % tmp用来保存每个寝室的共同爱好数
    for i = 1 : nums
        tmp(i) = get_total_hobby(path(:,i),mydata);  % 调用子函数单独计算每个寝室四名同学的共同爱好数
    end
%     ans = std(tmp);   % 计算标准差
    ans = (std(tmp) + 1)  / max(tmp);
%     ans = (std(tmp) + 1)  / mean(tmp);
end



